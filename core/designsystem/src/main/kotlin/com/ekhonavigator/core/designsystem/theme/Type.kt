 *   - body      → Normal      (long-form readability — generous 1.6 line height)
 *   - labels    → Medium      (M3 spec — chips, captions, monograms)
 *
 * Call sites that need a heavier accent (e.g. event-row titles) override locally
 * via `style = MaterialTheme.typography.titleSmall.copy(fontWeight = FontWeight.SemiBold)`.
